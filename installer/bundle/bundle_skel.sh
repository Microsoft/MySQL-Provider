#!/bin/sh

#
# Shell Bundle installer package for the MySQL project
#

set -e
PATH=/usr/bin:/usr/sbin:/bin:/sbin
umask 022

# Note: Because this is Linux-only, 'readlink' should work
SCRIPT="`readlink -e $0`"

# These symbols will get replaced during the bundle creation process.
#
# The PLATFORM symbol should contain ONE of the following:
#       Linux_REDHAT, Linux_SUSE, Linux_ULINUX
#
# The MYSQL_PKG symbol should contain something like:
#	mysql-cimprov-1.0.0-89.rhel.6.x64.  (script adds rpm or deb, as appropriate)

PLATFORM=<PLATFORM_TYPE>
MYSQL_PKG=<MYSQL_PKG>
SCRIPT_LEN=<SCRIPT_LEN>
SCRIPT_LEN_PLUS_ONE=<SCRIPT_LEN+1>

usage()
{
    echo "usage: $1 [OPTIONS]"
    echo "Options:"
    echo "  --extract              Extract contents and exit."
    echo "  --force                Force upgrade (override version checks)."
    echo "  --install              Install the package from the system."
    echo "  --purge                Uninstall the package and remove all related data."
    echo "  --remove               Uninstall the package from the system."
    echo "  --restart-deps         Reconfigure and restart dependent services (no-op)."
    echo "  --source-references    Show source code reference hashes."
    echo "  --upgrade              Upgrade the package in the system."
    echo "  --debug                use shell debug mode."
    echo "  -? | --help            shows this usage text."
}

source_references()
{
    cat <<EOF
-- Source code references --
EOF
}

cleanup_and_exit()
{
    if [ -n "$1" ]; then
        exit $1
    else
        exit 0
    fi
}

verifyNoInstallationOption()
{
    if [ -n "${installMode}" ]; then
        echo "$0: Conflicting qualifiers, exiting" >&2
        cleanup_and_exit 1
    fi

    return;
}

ulinux_detect_installer()
{
    INSTALLER=

    # If DPKG lives here, assume we use that. Otherwise we use RPM.
    type dpkg > /dev/null 2>&1
    if [ $? -eq 0 ]; then
        INSTALLER=DPKG
    else
        INSTALLER=RPM
    fi
}

# $1 - The filename of the package to be installed
pkg_add() {
    pkg_filename=$1
    case "$PLATFORM" in
        Linux_ULINUX)
            ulinux_detect_installer

            if [ "$INSTALLER" = "DPKG" ]; then
                dpkg --install --refuse-downgrade ${pkg_filename}.deb
            else
                rpm --install ${pkg_filename}.rpm
            fi
            ;;

        Linux_REDHAT|Linux_SUSE)
            rpm --install ${pkg_filename}.rpm
            ;;

        *)
            echo "Invalid platform encoded in variable \$PLATFORM; aborting" >&2
            cleanup_and_exit 2
    esac
}

# $1 - The package name of the package to be uninstalled
# $2 - Optional parameter. Only used when forcibly removing omi on SunOS
pkg_rm() {
    case "$PLATFORM" in
        Linux_ULINUX)
            ulinux_detect_installer
            if [ "$INSTALLER" = "DPKG" ]; then
                if [ "$installMode" = "P" ]; then
                    dpkg --purge $1
                else
                    dpkg --remove $1
                fi
            else
                rpm --erase $1
            fi
            ;;

        Linux_REDHAT|Linux_SUSE)
            rpm --erase $1
            ;;

        *)
            echo "Invalid platform encoded in variable \$PLATFORM; aborting" >&2
            cleanup_and_exit 2
    esac
}


# $1 - The filename of the package to be installed
pkg_upd() {
    pkg_filename=$1

    case "$PLATFORM" in
        Linux_ULINUX)
            ulinux_detect_installer
            if [ "$INSTALLER" = "DPKG" ]; then
                [ -z "${forceFlag}" ] && FORCE="--refuse-downgrade"
                dpkg --install $FORCE ${pkg_filename}.deb

                export PATH=/usr/local/sbin:/usr/sbin:/sbin:$PATH
            else
                [ -n "${forceFlag}" ] && FORCE="--force"
                rpm --upgrade $FORCE ${pkg_filename}.rpm
            fi
            ;;

        Linux_REDHAT|Linux_SUSE)
            [ -n "${forceFlag}" ] && FORCE="--force"
            rpm --upgrade $FORCE ${pkg_filename}.rpm
            ;;

        *)
            echo "Invalid platform encoded in variable \$PLATFORM; aborting" >&2
            cleanup_and_exit 2
    esac
}

force_stop_omi_service() {
    # For any installation or upgrade, we should be shutting down omiserver (and it will be started after install/upgrade).
    if [ -x /usr/sbin/invoke-rc.d ]; then
        /usr/sbin/invoke-rc.d omiserverd stop 1> /dev/null 2> /dev/null
    elif [ -x /sbin/service ]; then
        service omiserverd stop 1> /dev/null 2> /dev/null
    fi
 
    # Catchall for stopping omiserver
    /etc/init.d/omiserverd stop 1> /dev/null 2> /dev/null
    /sbin/init.d/omiserverd stop 1> /dev/null 2> /dev/null
}

#
# Executable code follows
#

while [ $# -ne 0 ]; do
    case "$1" in
        --extract-script)
            # hidden option, not part of usage
            # echo "  --extract-script FILE  extract the script to FILE."
            head -${SCRIPT_LEN} "${SCRIPT}" > "$2"
            local shouldexit=true
            shift 2
            ;;

        --extract-binary)
            # hidden option, not part of usage
            # echo "  --extract-binary FILE  extract the binary to FILE."
            tail +${SCRIPT_LEN_PLUS_ONE} "${SCRIPT}" > "$2"
            local shouldexit=true
            shift 2
            ;;

        --extract)
            verifyNoInstallationOption
            installMode=E
            shift 1
            ;;

        --force)
            forceFlag=true
            shift 1
            ;;

        --install)
            verifyNoInstallationOption
            installMode=I
            shift 1
            ;;

        --purge)
            verifyNoInstallationOption
            installMode=P
            shouldexit=true
            shift 1
            ;;

        --remove)
            verifyNoInstallationOption
            installMode=R
            shouldexit=true
            shift 1
            ;;

        --restart-deps)
            # No-op for MySQL, as there are no dependent services
            shift 1
            ;;

        --source-references)
            source_references
            cleanup_and_exit 0
            ;;

        --upgrade)
            verifyNoInstallationOption
            installMode=U
            shift 1
            ;;

        --debug)
            echo "Starting shell debug mode." >&2
            echo "" >&2
            echo "SCRIPT_INDIRECT: $SCRIPT_INDIRECT" >&2
            echo "SCRIPT_DIR:      $SCRIPT_DIR" >&2
            echo "SCRIPT:          $SCRIPT" >&2
            echo >&2
            set -x
            shift 1
            ;;

        -? | --help)
            usage `basename $0` >&2
            cleanup_and_exit 0
            ;;

        *)
            usage `basename $0` >&2
            cleanup_and_exit 1
            ;;
    esac
done

if [ -n "${forceFlag}" ]; then
    if [ "$installMode" != "I" -a "$installMode" != "U" ]; then
        echo "Option --force is only valid with --install or --upgrade" >&2
        cleanup_and_exit 1
    fi
fi

case "$PLATFORM" in
    Linux_REDHAT|Linux_SUSE|Linux_ULINUX)
        ;;

    *)
        echo "Invalid platform encoded in variable \$PLATFORM; aborting" >&2
        cleanup_and_exit 2
esac

if [ -z "${installMode}" ]; then
    echo "$0: No options specified, specify --help for help" >&2
    cleanup_and_exit 3
fi

# Do we need to remove the package?
set +e
if [ "$installMode" = "R" -o "$installMode" = "P" ]; then
    pkg_rm mysql-cimprov

    if [ "$installMode" = "P" ]; then
        echo "Purging all files in MySQL agent ..."
        rm -rf /etc/opt/microsoft/mysql-cimprov /opt/microsoft/mysql-cimprov /var/opt/microsoft/mysql-cimprov
    fi
fi

if [ -n "${shouldexit}" ]; then
    # when extracting script/tarball don't also install
    cleanup_and_exit 0
fi

#
# Do stuff before extracting the binary here, for example test [ `id -u` -eq 0 ],
# validate space, platform, uninstall a previous version, backup config data, etc...
#

#
# Extract the binary here.
#

echo "Extracting..."

# $PLATFORM is validated, so we know we're on Linux of some flavor
tail -n +${SCRIPT_LEN_PLUS_ONE} "${SCRIPT}" | tar xzf -
STATUS=$?
if [ ${STATUS} -ne 0 ]; then
    echo "Failed: could not extract the install bundle."
    cleanup_and_exit ${STATUS}
fi

#
# Do stuff after extracting the binary here, such as actually installing the package.
#

EXIT_STATUS=0

case "$installMode" in
    E)
        # Files are extracted, so just exit
        cleanup_and_exit ${STATUS}
        ;;

    I)
        echo "Installing MySQL agent ..."

        force_stop_omi_service

        pkg_add $MYSQL_PKG
        EXIT_STATUS=$?
        ;;

    U)
        echo "Updating MySQL agent ..."
        force_stop_omi_service

        pkg_upd $MYSQL_PKG
        EXIT_STATUS=$?
        ;;

    *)
        echo "$0: Invalid setting of variable \$installMode ($installMode), exiting" >&2
        cleanup_and_exit 2
esac

# Remove the package that was extracted as part of the bundle

[ -f $MYSQL_PKG.rpm ] && rm $MYSQL_PKG.rpm
[ -f $MYSQL_PKG.deb ] && rm $MYSQL_PKG.deb

if [ $? -ne 0 -o "$EXIT_STATUS" -ne "0" ]; then
    cleanup_and_exit 1
fi

cleanup_and_exit 0

#####>>- This must be the last line of this script, followed by a single empty line. -<<#####
